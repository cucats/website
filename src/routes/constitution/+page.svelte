<main class="bg-neutral-100">
    <section class="bg-tertiary-800 h-24"></section>
    <section>
        <div class="c-4 mx-auto max-w-5xl p-4 font-sans">
            <h1 class="my-8">Constitution</h1>
            <h2>Name</h2>
            <p>
                The name of the Society shall be "Cambridge University Computing and Technology
                Society", hereafter referred to as "the Society" or "CUCaTS". "The University of
                Cambridge" shall hereafter be referred to as "The University".
            </p>

            <h2>Aims and Objects</h2>
            <p>The aims and objects of the Society shall be:</p>
            <ul>
                <li>
                    To promote interest for members and the public in computing and information
                    technology
                </li>
                <li>To provide members with networking opportunities with potential employers</li>
                <li>
                    To provide a forum for discussion on issues and topics concerning information
                    technology
                </li>
                <li>
                    To provide members with funding and assistance in setting up and running their
                    own events that align with the society's aims
                </li>
            </ul>

            <h2>The Committee and Executive Committee</h2>

            <h3>Executive Committee</h3>

            <p>
                The day-to-day management of the Society is the responsibility of the Executive
                Committee. This body consists of:
            </p>
            <ul>
                <li>A President</li>
                <li>A Vice-President</li>
                <li>A Junior Treasurer</li>
                <li>A Secretary</li>
                <li>A Senior Treasurer</li>
            </ul>

            <h3>Senior Treasurer</h3>
            <p>
                The senior treasurer shall be a member of the Regent House, or other person approved
                by the Junior Proctor. The Senior Treasurer must be appointed by the elected members
                of The Executive Committee and must be ex officio a member of The Executive
                Committee. The Senior Treasurer shall ensure that there is in place proper finance,
                administration and regulation of the Society.
            </p>

            <h3>Decision Making</h3>
            <p>
                Hereafter, any decision mentioned as requiring the approval of the Executive
                Committee requires the approval of at least two of the President, Secretary and
                Junior Treasurer unless otherwise specified.
            </p>

            <h3>Delegation of Responsibilities</h3>
            <p>
                In order to aid with day-to-day operations, the Executive Committee may delegate
                responsibilities to other members entirely at its discretion. These members are
                hereafter referred to as the Committee and hold no special power unless explicitly
                specified by the Executive Committee in writing.
            </p>

            <h3>Elections</h3>
            <p>
                The Executive Committee, except for the post of Senior Treasurer, shall be first
                proposed and seconded by two other members. Then they shall be elected using Single
                Transferable Vote by a secret ballot if there are more than two candidates for a
                post, following candidate hustings at the Annual General Meeting (AGM). The voting
                process may be conducted online.
            </p>

            <h3 id="3.6">Nominations</h3>
            <p>
                Nominations for a new Executive Committee must be submitted to the outgoing
                President, who shall act as the Returning Officer for the Election.
            </p>

            <h3>Eligibility</h3>
            <p>
                Candidates for the Executive Committee must be Society Members in good-standing and
                current members of the University at the time of their election.
            </p>
            <p>
                Candidates for the roles of President, Vice-President, Junior Treasurer and
                Secretary shall be full-time undergraduate or master's students of the University of
                Cambridge.
            </p>

            <h3>Reopening of Nominations</h3>
            <p>
                Should no candidate stand for one or more of the positions or the election results
                in a call for reopening of nominations (RoN), these positions will be made open to
                all Society Members. The nomination process in <a href="#3.6">Section 3.6</a> must still
                be followed. Additional hustings may take place at the discretion of the Returning Officer.
            </p>

            <h3>Election Procedures</h3>
            <p>
                The timeline and procedures for each Election shall be set by the Returning Officer
                and made public on, or before, the date of opening nominations. Such procedures
                shall adhere to, and be interpreted in accordance with, relevant provisions of this
                Constitution.
            </p>

            <h3>Taking Office</h3>
            <p>
                The Executive Committee members elected in the annual Lent election shall take
                office at the Board of Advisors meeting or by the end of Easter Full Term, whichever
                is sooner.
            </p>

            <h3>Filling Vacancies</h3>
            <p>
                If any Executive position becomes vacant during the Full Tenure of the Executive
                Committee, the remaining Executives may appoint an existing member of the Society
                Committee to the vacant position. This appointment shall be agreed upon unanimously
                by the remaining members of the Executive Committee and documented. If such an
                appointment is not possible, an Election for the role shall be held following the
                procedures established for the most recent Election.
            </p>

            <h3 id="3.12">Removal from Office</h3>
            <p>
                In the event that two members of the Executive Committee lack confidence in the
                ability of the other member of the Executive Committee to fulfil the requirements of
                their position, that member can be removed from their position and a new member can
                be appointed. This can be appealed to the senior treasurer as outlined in <a
                    href="#9.4">Section 9.4</a
                >. Such a demotion also permanently disallows a member to become a member of the
                Executive Committee by default as an elected student representative.
            </p>

            <h2>Membership</h2>

            <h3>Eligibility</h3>
            <p>Membership of the society is open to:</p>
            <ul>
                <li>All members of the university</li>
                <li>
                    Any other interested individuals at the discretion of the Executive Committee.
                </li>
            </ul>

            <h3>Membership Fees</h3>
            <p>The Executive Committee may fix a membership fee at their discretion.</p>

            <h3>Honorary Membership</h3>
            <p>
                The Executive Committee and the General Meetings shall have the right to offer free
                temporary or life membership to any person as they see fit.
            </p>

            <h3>Suspension of Membership</h3>
            <p>
                The Executive Committee shall have the right to suspend the membership of any person
                whose actions it deems to be damaging to the Society or its reputation at its
                discretion. Suspensions may be appealed to the Senior Treasurer, whose decisions
                shall override the Executive Committee's. Such expulsion shall be undertaken in
                accordance with <a href="#9">Section 9</a>, and is subject to confirmation at the
                next General Meeting of the Society.
            </p>

            <h2>Financial and Liability Matters</h2>

            <h3>Banking</h3>
            <p>
                The Society must maintain a banking account with a suitable Bank or Building Society
                to hold its funds.
            </p>

            <h3>Junior Treasurer Responsibilities</h3>
            <p>
                It shall be the responsibility of the Junior Treasurer to ensure that monies
                received are properly accounted for, and that the Society's financial records are
                kept in good order. In particular, the Junior Treasurer must ensure continuity of
                Cambridge resident signatories for any bank accounts held by the Society.
            </p>

            <h3>Auditing</h3>
            <p>
                The Senior Treasurer shall make arrangements for the Society's Accounts to be
                properly audited, either by themselves, or by some other person approved under
                University Ordinances.
            </p>

            <h3>Senior Treasurer Liability</h3>
            <p>
                The Senior Treasurer shall not be liable for any financial debt or other obligation
                of the Society unless personally authorised such a debt in writing.
            </p>

            <h3>University Registration</h3>
            <p>
                For so long as the Society shall be Registered with the Junior Proctor, it shall be
                the duty of The Executive Committee to ensure that The Society complies with the
                requirements for Registration as a University Society.
            </p>

            <h3 id="5.6">Contractual Arrangements</h3>
            <p>
                The Executive Committee (acting by its members) is authorised to enter into
                contractual arrangements with third parties for and on behalf of all members, but
                only to the extent reasonably necessary for the proper performance of its duties
                pursuant to the constitution and acting always in the best interests of the Society
                and its members. Non-Executive Committee members are not entitled to enter into
                contractual arrangements for and on behalf of other members unless expressly
                authorised in writing by the Executive Committee.
            </p>

            <h3>Contractual Liability</h3>
            <p>
                When entering into contractual arrangements pursuant to <a href="#5.6"
                    >Section 5.6</a
                >, the Executive Committee shall endeavour to agree a contractual limit on the
                members' liability which does not exceed the assets of the Society from time to
                time, or, if not possible, a reasonable limit of liability taking into account the
                nature of the contract and the circumstances.
            </p>

            <h3>Fund Authorization</h3>
            <p>
                All monies drawn against Society funds should be authorised by at least two members
                of the Executive Committee (including the Junior or Senior Treasurer), either by
                signature, or access to online banking facilities
            </p>

            <h3>Trusteeship</h3>
            <p>
                The assets of the society shall be held in trust by the members of the Executive
                Committee as trustees for the benefit of the society and its members.
            </p>

            <h2>Amendments to the Constitution</h2>

            <h3>Amendment Process</h3>
            <p>
                The Constitution may be amended by a simple majority of those present at a Committee
                meeting, with a quorum of two-thirds of The Committee. Any amendments are subject to
                approval by the University Proctors as appropriate, and must be received by relevant
                parties within fourteen days of the vote.
            </p>

            <h3>Senior Treasurer Protection</h3>
            <p>
                No amendment to the constitution intended to remove the position of Senior
                Treasurer, to alter its prerogatives and duties, or to change the criteria and
                procedure for the appointment of a Senior Treasurer can be put to a vote without the
                prior written agreement of the Junior Proctor of the University of Cambridge.
            </p>

            <h2>Meetings</h2>

            <h3>Calling Meetings</h3>
            <p>Meetings can be called at any point by the President.</p>

            <h3>Chairing Meetings</h3>
            <p>
                The President, or in their absence the Vice-President, Secretary or Junior
                Treasurer, shall take the Chair at any meeting.
            </p>

            <h3>Meeting Minutes</h3>
            <p>
                Meeting minutes for every meeting shall be produced by the Secretary. In their
                absence, another member of the meeting should be elected to take minutes for that
                meeting.
            </p>
            <p>
                Minutes of the meeting must be available upon request for any member of the society.
            </p>

            <h3>Motions</h3>
            <p>
                Every motion at a General Meeting shall be proposed and seconded by two other
                members.
            </p>

            <h3>Extraordinary General Meetings</h3>
            <p>
                An Extraordinary General Meeting (EGM) may be held at any time during Full Term. It
                shall be held in Cambridge and may be called by the Executive Committee or at the
                written request of at least ten members. Twenty-one days written notice shall be
                given to members before an EGM is held. An EGM shall have the same powers as an AGM.
            </p>

            <h2>Equality of Opportunity</h2>
            <p>
                The society is committed in its pursuit of sporting excellence to equality of
                opportunity and to a proactive and inclusive approach to equality, which supports
                and encourages all under-represented groups, promotes an inclusive culture, and
                values diversity.
            </p>
            <p>
                The society is committed to everyone having the right to enjoy CUCaTS activities in
                an environment free from threat of intimidation, harassment and abuse.
            </p>
            <p>
                The society has a responsibility to oppose discriminatory behaviour and promote
                equality of opportunity.
            </p>
            <p>
                The society will deal with any incidence of discriminatory behaviour seriously, in
                accordance with the society's disciplinary procedures outlined in <a href="#9"
                    >Section 9</a
                >.
            </p>

            <h2 id="9">Disciplinary and Complaints Processes</h2>

            <h3>Authority to Discipline</h3>
            <p>
                The Executive Committee shall have the authority to expel or to suspend a member or
                members whose actions are felt to be such as to bring the society into disrepute, or
                to adversely affect the smooth running of the Society in a major way. This may only
                be performed by a majority vote of the Executive Committee members, excluding the
                Senior Treasurer.
            </p>

            <h3>Other Disciplinary Actions</h3>
            <p>
                The Executive Committee may also take other disciplinary action in respect of a
                member of the society, commensurate with the seriousness of the offence. The
                Executive Committee may take action as a result of member behaviour within any
                society-organised activities.
            </p>

            <h3>Consequences of Expulsion</h3>
            <p>
                In the event of an expulsion, the excluded member is required to return all
                equipment, documents and finances belonging to the society within 7 days. They will
                not be entitled to any full or partial refund of annual or lifetime membership fees.
            </p>

            <h3 id="9.4">Appeals Process</h3>
            <p>
                If the excluded or suspended member — or a former President, Vice-President, Junior
                Treasurer or Secretary that was demoted by the process specified in <a href="#3.12"
                    >Section 3.12</a
                > — wishes to appeal the decision, that appeal should be made to the Senior Treasurer
                who will consider all the facts and whose decision will be final. An appeal as to the
                process of exclusion, suspension or demotion, but not a further investigation into the
                facts, may be lodged with the Junior Proctor.
            </p>

            <h2>Dissolution of the Society</h2>

            <h3>Dissolution Process</h3>
            <p>
                The Society may be dissolved at a meeting provided that at least twenty-one days
                written notice of the intention to dissolve the Society has been given to the
                members. At least two thirds of those present and voting must vote in favour of the
                motion for Dissolution for it to be effective.
            </p>

            <h3>Asset Distribution</h3>
            <p>
                Any motion for Dissolution of the Society shall provide that assets remaining after
                all liabilities have been met shall be transferred either to another Registered
                University Society, or to the Societies' Syndicate.
            </p>

            <h2>Provision of Information</h2>
            <p>
                The Executive Committee must supply annual accounts to the Senior Treasurer and the
                University in accordance with the rules and regulations of university societies laid
                out by the Junior Proctor.
            </p>

            <h2>Declaration</h2>
            <p>
                CUCaTS hereby adopts and accepts this constitution as its current constitution
                regulating the actions of members and administrators, and will also comply with all
                University and legal requirements.
            </p>
        </div>
    </section>
